var import_obsidian5 = require("obsidian");
var AddItemModal = class extends import_obsidian5.Modal {
  constructor(app, properties, onSubmit) {
    super(app);
    this.itemName = "";
    this.propertyValues = {};
    this.description = "";
    this.properties = properties;
    this.onSubmit = onSubmit;
    for (const prop of properties) {
      if (prop.defaultValue !== void 0) {
        this.propertyValues[prop.name] = prop.defaultValue;
      }
    }
  }
  onOpen() {
    const { contentEl } = this;
    contentEl.empty();
    contentEl.addClass("checklist-modal");
    contentEl.createEl("h2", { text: "Add New Item" });
    new import_obsidian5.Setting(contentEl).setName("Name").setDesc("Name for the checklist item (also the filename)").addText(
      (text) => text.setPlaceholder("Item name").onChange((value) => {
        this.itemName = value;
      })
    );
    if (this.properties.length > 0) {
      contentEl.createEl("h3", { text: "Properties" });
    }
    for (const prop of this.properties) {
      this.renderPropertyField(contentEl, prop);
    }
    new import_obsidian5.Setting(contentEl).setName("Description").setDesc("Optional description for the item").addTextArea(
      (text) => text.setPlaceholder("Enter a description...").onChange((value) => {
        this.description = value;
      })
    );
    new import_obsidian5.Setting(contentEl).addButton(
      (button) => button.setButtonText("Add Item").setCta().onClick(() => {
        if (!this.itemName.trim()) {
          new import_obsidian5.Notice("Please enter an item name.");
          return;
        }
        this.onSubmit(
          this.itemName.trim(),
          { ...this.propertyValues },
          this.description.trim()
        );
        this.close();
      })
    );
  }
  onClose() {
    this.contentEl.empty();
  }
  /**
   * Renders an input field for a single property based on its type.
   */
  renderPropertyField(container, prop) {
    const setting = new import_obsidian5.Setting(container).setName(prop.name);
    switch (prop.type) {
      case "text":
      case "date":
        setting.addText(
          (text) => text.setPlaceholder(prop.type === "date" ? "YYYY-MM-DD" : `Enter ${prop.name}`).setValue(String(this.propertyValues[prop.name] || "")).onChange((value) => {
            this.propertyValues[prop.name] = value;
          })
        );
        break;
      case "number":
        setting.addText(
          (text) => text.setPlaceholder("0").setValue(String(this.propertyValues[prop.name] || "")).onChange((value) => {
            const num = Number(value);
            this.propertyValues[prop.name] = isNaN(num) ? value : num;
          })
        );
        break;
      case "checkbox":
        setting.addToggle(
          (toggle) => toggle.setValue(Boolean(this.propertyValues[prop.name])).onChange((value) => {
            this.propertyValues[prop.name] = value;
          })
        );
        break;
      case "dropdown":
        setting.addDropdown((dropdown) => {
          dropdown.addOption("", `Select ${prop.name}...`);
          for (const option of prop.options || []) {
            dropdown.addOption(option, option);
          }
          dropdown.setValue(String(this.propertyValues[prop.name] || "")).onChange((value) => {
            this.propertyValues[prop.name] = value;
          });
        });
        break;
    }
  }
};

